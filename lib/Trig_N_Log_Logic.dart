import 'dart:math';

class Logic {
  double Sin(double radians) {
    return sin(radians);
  }
  double Cos(double radians) {
    return cos(radians);
  }
  double Tan(double radians) {
    return tan(radians);
  }
  double Log(double input) {
    return log(input)/2.303; // dividing by 2.303 as  the function give log value for base e
  }

}







